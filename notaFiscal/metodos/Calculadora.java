package metodos;

public class Calculadora {
	
	public double somar(int n1, double n2) {
		double r = n1 +n2;
		return r;
	}
	
	public double multiplicar(double a, double b) {
		return a*b;
	}

	public double subtrair(double a, double b) {
		return a-b;
	}
	
	public double dividir(double a, double b) {
		return a/b;
	}
}
